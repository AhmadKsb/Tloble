class Checksuper {
  static const MethodChannel _channel = const MethodChannel('checksuper');

  static Future<bool> isDeviceRooted() async {
    final bool isRooted = await _channel.invokeMethod('checkRoot');
    return isRooted;
  }
}

Future<bool> checkIfSuperUser() async {
  print("[Root Check]: Started");
  print('isReleaseMode: $kReleaseMode');
  bool isRooted = false;
  if (kReleaseMode) {
    isRooted = await Checksuper.isDeviceRooted();
  }
  print("[Root Check]: Result - ${isRooted.toString()}");
  return isRooted;
}

String getNumberWithPrefixZero(int number) {
  return number.toString().length <= 1
      ? "0" + number.toString()
      : number.toString();
}

Future<void> requestGallery() async {
  if (Platform.isIOS) await requestPhotos();
  if (Platform.isAndroid) await requestStorage();
}

Future<void> requestPhotos() async {
  if (Platform.isAndroid) return null;
  await Permission.photos.request();
}

Future<void> requestStorage() async {
  await Permission.storage.request();
}

